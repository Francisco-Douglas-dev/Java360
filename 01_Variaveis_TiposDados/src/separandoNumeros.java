public class separandoNumeros {
    public static void main(String[] args){
        //Separando numeros em Java com -
        int numero1 = 1_000_000; // Usando underscore para separar milhares
        int numero2 = 123_456_789; // Usando underscore para separar milhares
        System.out.println("Número 1: " + numero1);
        System.out.println("Número 2: " + numero2);
    }
    
}
