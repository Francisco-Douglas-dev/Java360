public class separando_numeros {
    public static void main(String[] args) {
        // Exemplo de separação de números usando sublinhado para melhorar a legibilidade
        int numeroGrande = 1_000_000; // Um milhão
        long numeroMuitoGrande = 9_223_372_036_854_775_807L; // Valor máximo de um long
        double valorPi = 3.14159265358979323846; // Valor de Pi com alta precisão

        // Exibindo os valores das variáveis
        System.out.println("Número Grande: " + numeroGrande);
        System.out.println("Número Muito Grande: " + numeroMuitoGrande);
        System.out.println("Valor de Pi: " + valorPi);
    }
    
}
