public class tipo_char {
    public static void main(String[] args) {
        // Exemplo de uso do tipo char em Java
        char letra = 'A'; // Um único caractere entre aspas simples
        char simbolo = '@';
        char numero = '1';

        // Exibindo os valores dos caracteres
        System.out.println("Letra: " + letra);
        System.out.println("Símbolo: " + simbolo);
        System.out.println("Número: " + numero);

        // O tipo char é usado para representar um único caractere e ocupa 2 bytes na memória
    }
}
