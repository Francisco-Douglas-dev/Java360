public class castingImplicito {
    public static void main(String[] args){
        // Exemplo de casting implícito
        int numeroInteiro = 100;
        double numeroDecimal = numeroInteiro; // O int é convertido automaticamente para double

        System.out.println("Número inteiro: " + numeroInteiro);
        System.out.println("Número decimal (casting implícito): " + numeroDecimal);
    }
    
}
