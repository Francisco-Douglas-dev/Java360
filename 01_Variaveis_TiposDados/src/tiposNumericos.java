public class tiposNumericos {
    public static void main(String[] args) {
        // Tipos numéricos em Java

        // Tipo byte
        byte exemploByte = 127; // Valor máximo de um byte
        System.out.println("Valor do byte: " + exemploByte);

        // Tipo short
        short exemploShort = 32767; // Valor máximo de um short
        System.out.println("Valor do short: " + exemploShort);

        // Tipo int
        int exemploInt = 2147483647; // Valor máximo de um int
        System.out.println("Valor do int: " + exemploInt);

        // Tipo long
        long exemploLong = 9223372036854775807L; // Valor máximo de um long
        System.out.println("Valor do long: " + exemploLong);

        // Tipo float
        float exemploFloat = 3.14f; // Valor de ponto flutuante
        System.out.println("Valor do float: " + exemploFloat);

        // Tipo double
        double exemploDouble = 3.141592653589793; // Valor de ponto flutuante com maior precisão
        System.out.println("Valor do double: " + exemploDouble);
    }
}
