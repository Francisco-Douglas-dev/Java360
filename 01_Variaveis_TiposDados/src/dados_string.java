public class dados_string {
    public static void main(String[] args) {
        // Exemplo de uso do tipo String em Java
        String nome = "João"; // Uma sequência de caracteres entre aspas duplas
        String saudacao = "Olá, " + nome + "!"; // Concatenando strings

        // Exibindo a saudação
        System.out.println(saudacao);

        // O tipo String é usado para representar texto e é uma classe em Java, não um tipo primitivo
    }
}
