public class nomesVariaveis {
    public static void main(String[] args) {
        // Exemplos de nomes válidos para variáveis
        int idade = 25; // Nome simples e descritivo
        double salarioMensal = 3500.50; // Nome composto em camelCase
        char inicialDoNome = 'F'; // Nome descritivo para um caractere
        boolean estaAtivo = true; // Nome booleano com prefixo "esta"

        // Exibindo os valores das variáveis
        System.out.println("Idade: " + idade);
        System.out.println("Salário Mensal: " + salarioMensal);
        System.out.println("Inicial do Nome: " + inicialDoNome);
        System.out.println("Está Ativo: " + estaAtivo);

        // Exemplos de nomes inválidos (comentados para evitar erros de compilação)
        // int 1numero = 10; // Não pode começar com número
        // double salário-mensal = 5000.0; // Não pode conter traço
        // char @inicial = 'A'; // Não pode começar com símbolo especial
        // boolean public = false; // Não pode usar palavra reservada

        // Palavras reservadas em Java (não podem ser usadas como nomes de variáveis)
        // abstract, assert, boolean, break, byte, case, catch, char, class, const, continue,
        // default, do, double, else, enum, extends, final, finally, float, for, goto, if,
        // implements, import, instanceof, int, interface, long, native, new, package, private,
        // protected, public, return, short, static, strictfp, super, switch, synchronized,
        // this, throw, throws, transient, try, void, volatile, while
    }
}
