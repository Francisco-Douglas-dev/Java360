public class op_logicos {
    public static void main(String[] args) {
        // Operadores Lógicos
        boolean x = true;
        boolean y = false;
        
        System.out.println("x && y: " + (x && y)); // false
        System.out.println("x || y: " + (x || y)); // true
        System.out.println("!x: " + (!x)); // false
        System.out.println("!y: " + (!y)); // true
    }
}
