public class incremento_decremento {
    public static void main(String[] args) {
        int x = 5;

        // Incremento
        System.out.println("Valor de x antes do incremento: " + x);
        x++; // Incrementa x em 1
        System.out.println("Valor de x após o incremento: " + x);

        // Decremento
        System.out.println("Valor de x antes do decremento: " + x);
        x--; // Decrementa x em 1
        System.out.println("Valor de x após o decremento: " + x);
    }
}
