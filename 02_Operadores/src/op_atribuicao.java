public class op_atribuicao {
    public static void main(String[] args) {
        int a = 10; // Atribuição simples
        System.out.println("Valor de a: " + a);

        a += 5; // Atribuição composta (a = a + 5)
        System.out.println("Valor de a após += 5: " + a);

        a -= 3; // Atribuição composta (a = a - 3)
        System.out.println("Valor de a após -= 3: " + a);

        a *= 2; // Atribuição composta (a = a * 2)
        System.out.println("Valor de a após *= 2: " + a);

        a /= 4; // Atribuição composta (a = a / 4)
        System.out.println("Valor de a após /= 4: " + a);

        a %= 3; // Atribuição composta (a = a % 3)
        System.out.println("Valor de a após %= 3: " + a);
    }
    
}
